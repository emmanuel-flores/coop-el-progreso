				<h2 class="t-historia">LA COOPERATIVA DE HENDERSON SIEMPRE EN PLENO PROCESO DE CRECIMIENTO</h2>
				<p>Henderson, que es cabecera del partido de Hipólito Yrigoyen, cuenta con una superficie de 152.000 hecteareas. Después de muchos años de sufrir los efectos de las inundaciones, la población recupera sus esperanzas. La Cooperativa Agropecuaria «El Progreso», forma parte de esa nueva etapa en busca de su crecimiento.</p>
				<p>En asamblea de asociados, el 20 de octubre de 1948 quedó constituida la Cooperativa Agropecuaria “El Progreso” Ltda. de Henderson, y el 18 de abril de 1949, inició sus actividades en un local alquilado a Arcadio Gonzalo, que estaba ubicado en Hipólito Yrigoyen 59 de la localidad bonaerense. Por entonces, el personal que integraba la cooperativa eran el gerente, Eleuterio Lastra; el contador, Emilio Moyano; el encargado del almacén, Aureliano Carballo y el auxiliar Juan José Fernández.</p>
				<p>Dos años más tarde, se procedió a la compra de dos lotes en la calle Florida que ocupaban una superficie total de 995 metros cuadrados. La compra se hizo a la señora Aída de Spraggon y uno de los lotes contaba con una construcción. En 1955, la cooperativa adquiere otro lote de 500 m2 que linda con los anteriores. Los avances no se detuvieron y en 1957, se construyó en los terrenos adquiridos un edificio con salones de venta y oficinas para la administración que fueron inaugurados en marzo de 1958.</p>
				<p>Los hechos más significativos que marcaron hitos trascendentes en la historia de la entidad de Henderson indican, por ejemplo, que en 1960 se adquirió otro terreno de 195 metros cuadrados para ser destinado a la sección corralón. Dos años más tarde, 14 lotes de terreno de 10.549 metros fueron adquiridos por la cooperativa para ser destinados a instalar una planta de silos. Lindero a este predio, se compró en 1964 otro terreno de 200 metros cuadrados.</p>
