				<p>La sucesión de concreciones no se detuvo. Apuntando siempre a mejorar el servicio para el productor agropecuario, en 1968, se construyeron 12 silos con capacidad para 3984 toneladas, que se instalaron junto a una báscula para pesar camiones en terrenos del ferrocarril. Un año más tarde se adquirieron terrenos al Centro Juvenil Agrario y se inició la sección veterinaria.</p>
				<p>La construcción de seis nuevos silos de 3400 toneladas que se sumaron a los anteriores, se concretó en 1973 y al año siguiente se compró el terreno con edificación de la estación de servicio, con una superficie total de 895 metros cuadrados. La actividad en la estación de servicio YPF comenzó en 1975.</p>
				<p>Las concreciones prosiguieron en la Cooperativa de Henderson. La construcción de celdas y silos, y la compra de terrenos para la instalación de nuevas plantas, son sólo algunas de las metas alcanzadas por “El Progreso” en su inagotable afán de brindar más y mejores servicios a sus asociados.</p>
				<p>Los asociados de la Cooperativa “El Progreso” de Henderson pueden acceder al acopio de cereales, la compra de insumos, agroquímicos, fertilizantes y semillas, ferretería y compra de repuestos, servicio veterinario y agronómico, y autoservicio sobre el que se implementaron importantes obras de ampliación.</p>
